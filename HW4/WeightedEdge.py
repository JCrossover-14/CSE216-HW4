class WeightedEdge:
    def __init__(self,node1,node2,weight):
        self.node1=node1
        self.node2=node2
        self.weight=weight

    def getNode1(self):
        return self.node1
    
    def getNode2(self):
        return self.node2
    
    def getWeight(self):
        return self.weight
    
    
